
// Basic string examples

let singleQuoteString = 'Hello, World!';
let doubleQuoteString = "JavaScript is fun!";
let templateString = `This is a template literal.`;


// String concatenation

let firstName = "Alice";
let lastName = "Johnson";
let fullName = firstName + " " + lastName;
console.log(fullName);  // Output: Alice Johnson


// Template literal with expression

let age = 30;
let message = `I am ${age} years old.`;
console.log(message);  // Output: I am 30 years old.



// Escape characters

let stringWithQuotes = 'He said, "JavaScript is awesome!"';
let multiLineString = "This is line 1\nThis is line 2";
console.log(multiLineString);



// String length

let text = "Hello, JavaScript!";
console.log(text.length);  // Output: 17


// Accessing characters in a string


let char = text[0];
console.log(char);  // Output: H
