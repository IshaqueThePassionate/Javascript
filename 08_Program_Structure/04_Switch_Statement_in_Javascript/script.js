
// Switch Statement for Days of the Week

let day = 3;

switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("Thursday");
    break;
  case 5:
    console.log("Friday");
    break;
  case 6:
    console.log("Saturday");
    break;
  case 7:
    console.log("Sunday");
    break;
  default:
    console.log("Invalid day");
}



// Switch Statement for Fruits

let fruit = "apple";

switch (fruit) {
  case "banana":
    console.log("Bananas are yellow.");
    break;
  case "apple":
    console.log("Apples are red.");
    break;
  case "grape":
    console.log("Grapes are purple.");
    break;
  default:
    console.log("Unknown fruit.");
}



// Switch Statement for Grades Based on Score

let score = 85;

switch (true) {
  case score >= 90:
    console.log("A grade");
    break;
  case score >= 80:
    console.log("B grade");
    break;
  case score >= 70:
    console.log("C grade");
    break;
  case score >= 60:
    console.log("D grade");
    break;
  default:
    console.log("F grade");
}



// Switch Statement for Traffic Light

let trafficLight = "green";

switch (trafficLight) {
  case "red":
    console.log("Stop");
    break;
  case "yellow":
    console.log("Caution");
    break;
  case "green":
    console.log("Go");
    break;
  default:
    console.log("Invalid light color");
}



