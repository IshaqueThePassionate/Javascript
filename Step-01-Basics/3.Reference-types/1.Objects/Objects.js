
// JavaScript Objects

// Object Declaration


// Define an object representing a person
let person = {
    name: 'Alice',
    age: 25,
    city: 'New York',
    isStudent: true,
};

// Accessing Object Properties


// Accessing object properties using dot notation
console.log(person.name); // Output: Alice

// Accessing object properties using bracket notation
console.log(person['age']); // Output: 25


// Adding and Modifying Properties


// Adding a new property
person.gender = 'Female';

// Modifying an existing property
person.age = 30;

console.log(person.gender); // Output: Female
console.log(person.age);    // Output: 30


// Real-Life Object Example: Product


// Define an object representing a product
let product = {
    name: 'Smartphone',
    brand: 'Samsung',
    price: 599.99,
    inStock: true,
};


// Accessing Product Properties


// Accessing product properties using dot notation
console.log(product.name); // Output: Smartphone

// Accessing product properties using bracket notation
console.log(product['brand']); // Output: Samsung


// Modifying Product Properties


// Modifying an existing property
product.price = 549.99;

console.log(product.price); // Output: 549.99

